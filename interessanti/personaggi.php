<p><b>Brian May.</b> Nel 1965 si trasferisce a Londra, dove incomincia a studiare Fisica e Astronomia all'Imperial College London. Si laurea con lode in Fisica, intraprende poi il dottorato di ricerca in Astronomia, che abbandona per dedicarsi alla carriera musicale.</p>

